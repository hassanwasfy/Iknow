package com.abaferas.usecase.repositories

import com.abaferas.entities.Article
import com.abaferas.entities.ArticleList
import com.abaferas.entities.Books
import com.abaferas.entities.MostPopularArticle
import com.abaferas.entities.TopStories
import kotlinx.coroutines.flow.Flow

interface ILocalRepository {
    //region ArticleDao
    suspend fun insertArticle(article: Article)

    suspend fun deleteArticle(article: Article)

    fun getAllArticles(): Flow<List<Article>>
    suspend fun getArticleById(id: Long): Flow<Article>
    //endregion

    //region BooksDao
    suspend fun insertBook(books: Books)

    suspend fun deleteBook(books: Books)

    fun getAllBooks(): Flow<List<Books>>
    suspend fun getBookById(id: Long): Flow<Books>
    //endregion

    //region ArticleListDao
    suspend fun insertArticleList(articleList: ArticleList)

    suspend fun deleteArticleList(articleList: ArticleList)

    fun getAllArticleLists(): Flow<List<ArticleList>>
    suspend fun getArticleListById(id: Long): Flow<ArticleList>
    //endregion

    //region MostPopularDao
    suspend fun insertMostPopularArticle(mostPopularArticle: MostPopularArticle)

    suspend fun deleteMostPopularArticle(mostPopularArticle: MostPopularArticle)

    fun getAllMostPopularArticles(): Flow<List<MostPopularArticle>>
    suspend fun getMostPopularArticleById(id: Long): Flow<MostPopularArticle>
    //endregion

    //region TopStoryDao
    suspend fun insertTopStory(topStories: TopStories)

    suspend fun deleteTopStory(topStories: TopStories)

    fun getAllTopStories(): Flow<List<TopStories>>
    suspend fun getTopStoryById(id: Long): Flow<TopStories>
    //endregion
}